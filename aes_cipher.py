# =========================================================
# BASIC HELPERS
# =========================================================

def bytes_to_matrix(data):
    return [list(data[i:i + 4]) for i in range(0, len(data), 4)]


def matrix_to_bytes(matrix):
    result = []
    for row in matrix:
        result += row
    return bytes(result)


def xor_bytes(a, b):
    return bytes(i ^ j for i, j in zip(a, b))


def pad(data):
    padding_len = 16 - (len(data) % 16)
    return data + bytes([padding_len]) * padding_len


def unpad(data):
    if len(data) == 0:
        raise ValueError("Invalid data")

    padding_len = data[-1]

    if padding_len < 1 or padding_len > 16:
        raise ValueError("Invalid padding or wrong key")

    if data[-padding_len:] != bytes([padding_len]) * padding_len:
        raise ValueError("Invalid padding or corrupted cipher")

    return data[:-padding_len]


# =========================================================
# AES CORE STEPS
# =========================================================

def add_round_key(state, round_key):
    for i in range(4):
        for j in range(4):
            state[i][j] ^= round_key[i][j]


def sub_bytes(state):
    for i in range(4):
        for j in range(4):
            state[i][j] = S_BOX[state[i][j]]


def inv_sub_bytes(state):
    for i in range(4):
        for j in range(4):
            state[i][j] = INV_S_BOX[state[i][j]]


def shift_rows(state):
    state[0][1], state[1][1], state[2][1], state[3][1] = state[1][1], state[2][1], state[3][1], state[0][1]
    state[0][2], state[1][2], state[2][2], state[3][2] = state[2][2], state[3][2], state[0][2], state[1][2]
    state[0][3], state[1][3], state[2][3], state[3][3] = state[3][3], state[0][3], state[1][3], state[2][3]


def inv_shift_rows(state):
    state[0][1], state[1][1], state[2][1], state[3][1] = state[3][1], state[0][1], state[1][1], state[2][1]
    state[0][2], state[1][2], state[2][2], state[3][2] = state[2][2], state[3][2], state[0][2], state[1][2]
    state[0][3], state[1][3], state[2][3], state[3][3] = state[1][3], state[2][3], state[3][3], state[0][3]


def xtime(a):
    if a & 0x80:
        return ((a << 1) ^ 0x1B) & 0xFF
    return a << 1


def multiply(a, b):
    result = 0

    while b:
        if b & 1:
            result ^= a

        a = xtime(a)
        b >>= 1

    return result


def mix_single_column(column):
    a0, a1, a2, a3 = column

    column[0] = multiply(a0, 2) ^ multiply(a1, 3) ^ a2 ^ a3
    column[1] = a0 ^ multiply(a1, 2) ^ multiply(a2, 3) ^ a3
    column[2] = a0 ^ a1 ^ multiply(a2, 2) ^ multiply(a3, 3)
    column[3] = multiply(a0, 3) ^ a1 ^ a2 ^ multiply(a3, 2)


def mix_columns(state):
    for column in state:
        mix_single_column(column)


def inv_mix_single_column(column):
    a0, a1, a2, a3 = column

    column[0] = multiply(a0, 14) ^ multiply(a1, 11) ^ multiply(a2, 13) ^ multiply(a3, 9)
    column[1] = multiply(a0, 9) ^ multiply(a1, 14) ^ multiply(a2, 11) ^ multiply(a3, 13)
    column[2] = multiply(a0, 13) ^ multiply(a1, 9) ^ multiply(a2, 14) ^ multiply(a3, 11)
    column[3] = multiply(a0, 11) ^ multiply(a1, 13) ^ multiply(a2, 9) ^ multiply(a3, 14)


def inv_mix_columns(state):
    for column in state:
        inv_mix_single_column(column)


# =========================================================
# KEY EXPANSION
# =========================================================

def rotate_word(word):
    return word[1:] + word[:1]


def sub_word(word):
    return [S_BOX[b] for b in word]


def key_expansion(key):
    key_len = len(key)

    if key_len not in [16, 24, 32]:
        raise ValueError("Key must be 16, 24, or 32 bytes")

    nk = key_len // 4

    if nk == 4:
        nr = 10
    elif nk == 6:
        nr = 12
    else:
        nr = 14

    key_words = bytes_to_matrix(key)

    i = nk

    while len(key_words) < 4 * (nr + 1):
        temp = key_words[-1].copy()

        if i % nk == 0:
            temp = rotate_word(temp)
            temp = sub_word(temp)
            temp[0] ^= R_CON[i // nk]

        elif nk > 6 and i % nk == 4:
            temp = sub_word(temp)

        new_word = []

        for j in range(4):
            new_word.append(key_words[i - nk][j] ^ temp[j])

        key_words.append(new_word)
        i += 1

    round_keys = []

    for i in range(0, len(key_words), 4):
        round_keys.append(key_words[i:i + 4])

    return round_keys, nr


# =========================================================
# AES BLOCK ENCRYPT / DECRYPT
# =========================================================

def encrypt_block(block, round_keys, nr):
    if len(block) != 16:
        raise ValueError("Block must be exactly 16 bytes")

    state = bytes_to_matrix(block)

    add_round_key(state, round_keys[0])

    for round_number in range(1, nr):
        sub_bytes(state)
        shift_rows(state)
        mix_columns(state)
        add_round_key(state, round_keys[round_number])

    sub_bytes(state)
    shift_rows(state)
    add_round_key(state, round_keys[nr])

    return matrix_to_bytes(state)


def decrypt_block(block, round_keys, nr):
    if len(block) != 16:
        raise ValueError("Block must be exactly 16 bytes")

    state = bytes_to_matrix(block)

    add_round_key(state, round_keys[nr])
    inv_shift_rows(state)
    inv_sub_bytes(state)

    for round_number in range(nr - 1, 0, -1):
        add_round_key(state, round_keys[round_number])
        inv_mix_columns(state)
        inv_shift_rows(state)
        inv_sub_bytes(state)

    add_round_key(state, round_keys[0])

    return matrix_to_bytes(state)


# =========================================================
# AES CBC MODE ENCRYPT / DECRYPT
# =========================================================

def encrypt(plain_text, key):
    key = key.encode("utf-8")

    round_keys, nr = key_expansion(key)

    data = plain_text.encode("utf-8")
    data = pad(data)

    iv = os.urandom(16)
    previous_block = iv

    encrypted_data = b""

    for i in range(0, len(data), 16):
        block = data[i:i + 16]

        block = xor_bytes(block, previous_block)

        encrypted_block = encrypt_block(block, round_keys, nr)

        encrypted_data += encrypted_block

        previous_block = encrypted_block

    final_result = iv + encrypted_data

    return base64.b64encode(final_result).decode("utf-8")


def decrypt(cipher_text, key):
    key = key.encode("utf-8")

    round_keys, nr = key_expansion(key)

    try:
        data = base64.b64decode(cipher_text)
    except Exception:
        raise ValueError("Cipher text must be valid Base64")

    if len(data) < 32 or len(data) % 16 != 0:
        raise ValueError("Invalid cipher text length")

    iv = data[:16]
    encrypted_data = data[16:]

    previous_block = iv
    decrypted_data = b""

    for i in range(0, len(encrypted_data), 16):
        encrypted_block = encrypted_data[i:i + 16]

        decrypted_block = decrypt_block(encrypted_block, round_keys, nr)

        original_block = xor_bytes(decrypted_block, previous_block)

        decrypted_data += original_block

        previous_block = encrypted_block

    decrypted_data = unpad(decrypted_data)

    return decrypted_data.decode("utf-8")


